import importlib.util
import json
import os
import re
from pathlib import Path
from collections import defaultdict, Counter


def load_module(path, name):
    spec = importlib.util.spec_from_file_location(name, str(path))
    mod = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)
    return mod


def norm_seq(s):
    return re.sub(r"[^A-Za-z]", "", str(s or "")).upper().replace("*", "")


def read_json(path):
    with open(path, encoding="utf-8") as f:
        return json.load(f)


def parse_fasta_like(path):
    records = []
    header = None
    seq = []

    try:
        with open(path, encoding="utf-8", errors="ignore") as f:
            for line in f:
                line = line.strip()
                if not line:
                    continue
                if line.startswith(">"):
                    if header is not None:
                        records.append((header, norm_seq("".join(seq))))
                    header = line[1:].strip()
                    seq = []
                else:
                    if header is not None:
                        seq.append(line)
        if header is not None:
            records.append((header, norm_seq("".join(seq))))
    except Exception:
        return []

    return [(h, s) for h, s in records if h]


def maybe_text_contains_pdb(path, pdb_id, max_bytes=20000):
    pdb_id = str(pdb_id or "").lower()
    if not pdb_id:
        return True
    name = path.name.lower()
    if pdb_id in name:
        return True
    try:
        with open(path, "rb") as f:
            data = f.read(max_bytes).decode("utf-8", errors="ignore").lower()
        return pdb_id in data
    except Exception:
        return False


def same_stem_aux_files(source_path):
    out = []
    p = Path(source_path)
    if not p.exists():
        return out

    parent = p.parent
    stem = p.stem

    suffixes = [
        ".toRCSB", ".fasta", ".fa", ".faa", ".cif",
        ".manAlign", ".autoAlign", ".fullLen",
        ".autoDel", ".manDel",
    ]

    for suf in suffixes:
        q = parent / f"{stem}{suf}"
        if q.exists():
            out.append(q)

    # Also include any same-stem files with unusual exact suffixes.
    try:
        for q in parent.iterdir():
            if q.is_file() and q.name.startswith(stem):
                if q not in out:
                    out.append(q)
    except Exception:
        pass

    return out


def collect_aux_files(entry_id, pdb_id, chain_json, web_roots, batch_root=None, max_batch_hits=200, aux_index_path=None):
    files = set()

    # 1. source_files siblings
    sf = chain_json.get("source_files", {})
    for key in ["structure_path", "fasta_path"]:
        p = sf.get(key)
        if p:
            p = Path(p)
            if p.exists():
                files.add(p)
            try:
                rp = p.resolve()
                if rp.exists():
                    files.add(rp)
                    for q in same_stem_aux_files(rp):
                        files.add(q)
            except Exception:
                pass
            for q in same_stem_aux_files(p):
                files.add(q)

    # 2. web roots, entry stem
    for root in web_roots:
        root = Path(root)
        if not root.exists():
            continue
        for suf in [".toRCSB", ".fasta", ".fa", ".faa", ".cif", ".manAlign", ".autoAlign", ".fullLen", ".autoDel", ".manDel"]:
            q = root / f"{entry_id}{suf}"
            if q.exists():
                files.add(q)

    # 3. batch root; use carefully, filter by pdb_id in file name or content
    if batch_root:
        batch_root = Path(batch_root)
        if batch_root.exists():
            patterns = [entry_id, str(pdb_id).lower(), str(pdb_id).upper()]
            hits = []
            for pat in patterns:
                if not pat:
                    continue
                try:
                    hits.extend(batch_root.rglob(f"*{pat}*"))
                except Exception:
                    pass
                if len(hits) >= max_batch_hits:
                    break

            for q in hits[:max_batch_hits]:
                if q.is_file() and maybe_text_contains_pdb(q, pdb_id):
                    files.add(q)

    add_aux_files_from_index(files, pdb_id, aux_index_path)

    return sorted(files, key=lambda x: str(x))



def add_aux_files_from_index(files, pdb_id, aux_index_path):
    """
    Add auxiliary files by pdb_id from a pre-built batch_aux_file_index.csv.
    This solves the case where files under example_data/raw/batch
    are named by internal IDs instead of entry_id or pdb_id.
    """
    if not aux_index_path:
        return

    idx = Path(aux_index_path)
    if not idx.exists():
        return

    target = str(pdb_id or "").strip().lower()
    if not target:
        return

    try:
        with open(idx, newline="", encoding="utf-8", errors="ignore") as f:
            reader = csv.DictReader(f)
            for r in reader:
                if str(r.get("pdb_id", "")).strip().lower() != target:
                    continue
                q = Path(r.get("path", ""))
                if q.exists():
                    files.add(q)
    except Exception:
        return

def parse_torcsb_files(files, pdb_id):
    """
    Parse .toRCSB headers from emdbMatch.py.

    emdbMatch writes:
        rcsbL.append(f">{t2c[t]}_{n}\\n{n2seq[n]}\\n")

    Typical header:
        6gyp_2_A_B
    Interpreted as:
        pdb_id = 6gyp
        entity = 2
        rcsb_chain = A
        local_chain = B
    """
    mapping = {}
    examples = []

    for f in files:
        if not str(f).endswith(".toRCSB"):
            continue

        for header, seq in parse_fasta_like(f):
            token = header.split()[0]
            parts = token.split("_")

            if len(parts) >= 4 and parts[0].lower() == str(pdb_id).lower():
                local_chain = parts[-1]
                rcsb_chain = parts[-2]
                entity_id = parts[-3]
                mapping[local_chain] = {
                    "rcsb_chain": rcsb_chain,
                    "entity_id": entity_id,
                    "header": header,
                    "file": str(f),
                    "sequence": seq,
                }
                examples.append((local_chain, rcsb_chain, header, str(f)))

    return mapping, examples


def parse_cif_struct_ref(files, pdb_id):
    """
    Parse mmCIF struct_ref and struct_ref_seq to obtain chain -> UniProt accession hints.
    Returns dict: chain_id -> set(accessions)
    """
    out = defaultdict(set)

    try:
        from Bio.PDB.MMCIF2Dict import MMCIF2Dict
    except Exception:
        return out

    def as_list(x):
        if x is None:
            return []
        if isinstance(x, list):
            return x
        return [x]

    cif_files = [f for f in files if str(f).endswith(".cif") and maybe_text_contains_pdb(f, pdb_id)]
    for cf in cif_files:
        try:
            d = MMCIF2Dict(str(cf))
        except Exception:
            continue

        ref_ids = as_list(d.get("_struct_ref.id"))
        db_names = as_list(d.get("_struct_ref.db_name"))
        accessions = as_list(d.get("_struct_ref.pdbx_db_accession")) or as_list(d.get("_struct_ref.db_code"))

        ref_to_acc = {}
        for rid, dbn, acc in zip(ref_ids, db_names, accessions):
            dbn = str(dbn or "")
            acc = str(acc or "").strip()
            if not acc or acc in {".", "?"}:
                continue
            # UniProt is often UNP in PDB mmCIF.
            if "UNP" in dbn.upper() or "UNIPROT" in dbn.upper():
                ref_to_acc[str(rid)] = acc

        seq_ref_ids = as_list(d.get("_struct_ref_seq.ref_id"))
        strands = as_list(d.get("_struct_ref_seq.pdbx_strand_id"))
        pdb_codes = as_list(d.get("_struct_ref_seq.pdbx_PDB_id_code"))

        for rid, strand, code in zip(seq_ref_ids, strands, pdb_codes):
            rid = str(rid)
            acc = ref_to_acc.get(rid)
            if not acc:
                continue

            # If pdb code exists, prefer matched pdb_id.
            if code and str(code).lower() not in {".", "?", str(pdb_id).lower()}:
                continue

            for ch in re.split(r"[,; ]+", str(strand or "")):
                ch = ch.strip()
                if ch and ch not in {".", "?"}:
                    out[ch].add(acc)

    return out


def accessions_from_sifts_chain(idx, rcsb_chain):
    accs = set()
    for r in idx.get("rows_by_chain", {}).get(rcsb_chain, []):
        acc = r.get("uniprot_acc")
        if acc:
            accs.add(acc)
    return accs


def classify_problem(row):
    q = row.get("mapping_quality", "")
    st = row.get("status", "")

    if q == "" or pd_blank(row):
        return "blank"
    if q == "FAILED" or st == "failed":
        return "failed"

    cov = safe_float(row.get("coverage"))
    ident = safe_float(row.get("identity"))

    if cov >= 0.8 and ident < 0.9:
        return "high_coverage_low_identity"
    if cov < 0.2 and ident >= 0.9:
        return "very_low_coverage_high_identity"
    if cov < 0.2 and ident < 0.9:
        return "very_low_coverage_low_identity"
    if cov < 0.8 and ident < 0.9:
        return "partial_coverage_low_identity"
    return "other_problem"


def pd_blank(row):
    return not str(row.get("mapping_quality", "")).strip()


def safe_float(x):
    try:
        if x is None or str(x).strip() == "":
            return 0.0
        return float(x)
    except Exception:
        return 0.0


def build_final_annotations(mod, base, fmap, idx, cache_dir, feature_types):
    feature_by_acc = {}
    for acc in fmap.get("uniprot_accessions", []):
        try:
            uj = mod.fetch_uniprot_json(acc, cache_dir, base=base)
            feature_by_acc[acc] = mod.extract_uniprot_features(uj, feature_types, include_all=False)
        except Exception:
            feature_by_acc[acc] = []

    uf = mod.convert_uniprot_features(fmap, feature_by_acc)
    pfam = mod.convert_sifts_xrefs(fmap, idx.get("xref_rows", []), "Pfam")
    interpro = mod.convert_sifts_xrefs(fmap, idx.get("xref_rows", []), "InterPro")
    external = mod.merge_external(uf, pfam, interpro)
    return uf, pfam, interpro, external


def repair_one(row, args, mod, base, sifts_cache, feature_types):
    jf = Path(row["json_path"])
    if not jf.exists():
        return {"repair_status": "missing_json", "repair_note": str(jf)}

    d = read_json(jf)
    entry_id = d.get("entry", {}).get("entry_id", row.get("entry_id", jf.parent.parent.name))
    pdb_id = str(d.get("entry", {}).get("pdb_id", row.get("pdb_id", ""))).strip().lower()
    chain_id = str(d.get("chain", {}).get("chain_id", row.get("chain_id", jf.stem))).strip()
    chain_seq = norm_seq(d.get("chain", {}).get("sequence", ""))

    if not pdb_id:
        return {"repair_status": "no_pdb_id", "repair_note": "pdb_id is empty"}

    idx = mod.load_sifts_index(pdb_id, Path(args.cache_dir), base, sifts_cache)

    web_roots = [x for x in args.web_roots.split(",") if x.strip()]
    aux_files = collect_aux_files(
        entry_id=entry_id,
        pdb_id=pdb_id,
        chain_json=d,
        web_roots=web_roots,
        batch_root=args.batch_root if args.use_batch_root else None,
        max_batch_hits=args.max_batch_hits,
        aux_index_path=args.aux_index,
    )

    torcsb_map, torcsb_examples = parse_torcsb_files(aux_files, pdb_id)
    cif_accs = parse_cif_struct_ref(aux_files, pdb_id)

    candidate_accs = []
    candidate_sources = defaultdict(list)

    # Previous accession from problem row / JSON
    for acc in str(row.get("uniprot_accessions", "") or "").split(";"):
        acc = acc.strip()
        if acc:
            candidate_accs.append(acc)
            candidate_sources[acc].append("previous_problem_row")

    # All SIFTS accession is broad fallback.
    for acc in idx.get("uniprot_accessions", []):
        if acc:
            candidate_accs.append(acc)
            candidate_sources[acc].append("sifts_all_accessions")

    # .toRCSB-guided RCSB chain -> SIFTS accession
    rcsb_chain = None
    if chain_id in torcsb_map:
        rcsb_chain = torcsb_map[chain_id]["rcsb_chain"]
        for acc in accessions_from_sifts_chain(idx, rcsb_chain):
            candidate_accs.insert(0, acc)
            candidate_sources[acc].append(f"toRCSB_chain:{chain_id}->{rcsb_chain}")

    # mmCIF struct_ref accessions, local chain and RCSB chain.
    for ch in [chain_id, rcsb_chain]:
        if not ch:
            continue
        for acc in cif_accs.get(ch, []):
            candidate_accs.insert(0, acc)
            candidate_sources[acc].append(f"cif_struct_ref_chain:{ch}")

    # Deduplicate, preserve order.
    seen = set()
    candidate_accs2 = []
    for acc in candidate_accs:
        if acc and acc not in seen:
            seen.add(acc)
            candidate_accs2.append(acc)

    if not candidate_accs2:
        return {
            "repair_status": "no_candidate_accession",
            "repair_note": "no candidates from previous/SIFTS/toRCSB/cif",
            "aux_file_count": len(aux_files),
        }

    thresholds = (
        args.high_coverage, args.high_identity,
        args.ok_coverage, args.ok_identity,
        args.partial_coverage
    )

    # Score candidates by current chain.sequence vs UniProt sequence.
    scored = []
    for acc in candidate_accs2[:args.max_candidates]:
        try:
            uni_seq = mod.get_uniprot_sequence(acc, Path(args.cache_dir), base=base)
        except Exception:
            try:
                uj = mod.fetch_uniprot_json(acc, Path(args.cache_dir), base=base)
                seq = uj.get("sequence", {})
                uni_seq = norm_seq(seq.get("value", "") if isinstance(seq, dict) else "")
            except Exception:
                continue

        if not uni_seq:
            continue

        try:
            fmap = mod.alignment_mapping_object(
                d, acc, uni_seq, thresholds,
                source_note="auxiliary_file_guided_repair"
            )
        except TypeError:
            # For variants of 07b with different signature names.
            fmap = mod.alignment_mapping_object(d, acc, uni_seq, thresholds, "auxiliary_file_guided_repair")

        sources = sorted(set(candidate_sources.get(acc, [])))
        fmap["repair_candidate_sources"] = sources
        scored.append(fmap)

    scored.sort(key=lambda x: (
        1 if x.get("quality") in {"HIGH", "ACCEPTABLE", "PARTIAL_USABLE"} else 0,
        safe_float(x.get("identity")),
        safe_float(x.get("coverage")),
        int(x.get("mapped_count", 0) or 0),
    ), reverse=True)

    if not scored:
        return {
            "repair_status": "no_alignment_scored",
            "repair_note": "candidate accessions could not be aligned",
            "candidate_count": len(candidate_accs2),
            "aux_file_count": len(aux_files),
        }

    best = scored[0]
    trusted = best.get("quality") in {"HIGH", "ACCEPTABLE", "PARTIAL_USABLE"}

    repair_info = {
        "repair_status": "rescued" if trusted else "not_rescued",
        "entry_id": entry_id,
        "pdb_id": pdb_id,
        "chain_id": chain_id,
        "old_quality": row.get("mapping_quality", ""),
        "old_coverage": row.get("coverage", ""),
        "old_identity": row.get("identity", ""),
        "new_quality": best.get("quality", ""),
        "new_status": best.get("status", ""),
        "new_method": "auxiliary_file_guided_alignment_repair",
        "new_accessions": ";".join(best.get("uniprot_accessions", [])),
        "new_mapped_count": best.get("mapped_count", 0),
        "new_chain_length": best.get("chain_length", ""),
        "new_coverage": best.get("coverage", 0),
        "new_identity": best.get("identity", 0),
        "candidate_count": len(candidate_accs2),
        "top_candidate_sources": "|".join(best.get("repair_candidate_sources", [])),
        "torcsb_chain": rcsb_chain or "",
        "aux_file_count": len(aux_files),
        "json_path": str(jf),
        "repair_note": best.get("reason", ""),
    }

    if args.write and trusted:
        best["source"] = "Auxiliary file guided UniProt sequence alignment"
        best["final_mapping_method"] = "auxiliary_file_guided_alignment_repair"
        best["repair"] = {
            "used_auxiliary_files": True,
            "toRCSB_chain": rcsb_chain,
            "candidate_sources": best.get("repair_candidate_sources", []),
            "aux_file_examples": [str(x) for x in aux_files[:20]],
            "previous_mapping_quality": row.get("mapping_quality", ""),
            "previous_coverage": row.get("coverage", ""),
            "previous_identity": row.get("identity", ""),
        }

        uf, pfam, interpro, external = build_final_annotations(
            mod, base, best, idx, Path(args.cache_dir), feature_types
        )

        d["uniprot_mapping_before_repair"] = d.get("uniprot_mapping", {})
        d["uniprot_mapping"] = best
        d["external_annotations"] = external
        d.setdefault("tracks", {})
        d["tracks"]["uniprot_features"] = {
            "status": uf["status"],
            "type": "features",
            "source": uf["source"],
            "coordinate_system": "chain_index_1_based",
            "items": uf["items"],
        }
        d["tracks"]["pfam"] = {
            "status": pfam["status"],
            "type": "features",
            "source": pfam["source"],
            "coordinate_system": "chain_index_1_based",
            "items": pfam["items"],
        }
        d["tracks"]["interpro"] = {
            "status": interpro["status"],
            "type": "features",
            "source": interpro["source"],
            "coordinate_system": "chain_index_1_based",
            "items": interpro["items"],
        }

        d.setdefault("status", {})
        d["status"]["uniprot_mapping"] = best.get("status", "")
        d["status"]["uniprot_mapping_quality"] = best.get("quality", "")
        d["status"]["uniprot_mapping_method"] = best.get("final_mapping_method", "")
        d["status"]["external_annotations"] = external.get("status", "")
        d["status"]["uniprot_features"] = uf.get("status", "")
        d["status"]["pfam"] = pfam.get("status", "")
        d["status"]["interpro"] = interpro.get("status", "")

        mod.append_log(
            d,
            "repair_problem_chain_auxiliary_files",
            "rescued",
            (
                f"Problem chain repaired using auxiliary files. "
                f"method=auxiliary_file_guided_alignment_repair; "
                f"quality={best.get('quality')}; coverage={best.get('coverage')}; "
                f"identity={best.get('identity')}; acc={best.get('uniprot_accessions')}; "
                f"toRCSB_chain={rcsb_chain}; sources={best.get('repair_candidate_sources')}"
            )
        )

        mod.write_json_atomic(jf, d)

        repair_info.update({
            "external_annotation_status": external.get("status", ""),
            "uniprot_feature_count": uf.get("item_count", 0),
            "pfam_count": pfam.get("item_count", 0),
            "interpro_count": interpro.get("item_count", 0),
        })

    elif args.write and (not trusted) and args.write_failed_marker:
        d.setdefault("repair_attempts", [])
        d["repair_attempts"].append({
            "method": "auxiliary_file_guided_alignment_repair",
            "status": "not_rescued",
            "best_quality": best.get("quality", ""),
            "best_coverage": best.get("coverage", ""),
            "best_identity": best.get("identity", ""),
            "candidate_count": len(candidate_accs2),
            "toRCSB_chain": rcsb_chain,
        })
        mod.write_json_atomic(jf, d)

    return repair_info


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--problem-csv", default="example_outputs/qc/sifts_uniprot_alignment_fallback_problem_chains.csv")
    ap.add_argument("--summary-out", default="example_outputs/qc/problem_chain_aux_repair_summary.csv")
    ap.add_argument("--map-script", default="scripts/07b_sifts_alignment_fallback.py")
    ap.add_argument("--base-script", default="scripts/07_sifts_uniprot_annotations.py")
    ap.add_argument("--cache-dir", default=".cache/sifts_uniprot")
    ap.add_argument("--web-roots", default="example_data/raw/web")
    ap.add_argument("--batch-root", default="example_data/raw/batch")
    ap.add_argument("--use-batch-root", action="store_true")
    ap.add_argument("--aux-index", default="example_outputs/qc/batch_aux_file_index.csv",
                    help="CSV index mapping pdb_id to auxiliary files under batch root.")
    ap.add_argument("--max-batch-hits", type=int, default=200)
    ap.add_argument("--max-candidates", type=int, default=120)
    ap.add_argument("--limit", type=int, default=0)
    ap.add_argument("--only-class", default="", help="e.g. high_coverage_low_identity, failed, very_low_coverage_low_identity")
    ap.add_argument("--write", action="store_true")
    ap.add_argument("--write-failed-marker", action="store_true")
    ap.add_argument("--high-coverage", type=float, default=0.95)
    ap.add_argument("--high-identity", type=float, default=0.98)
    ap.add_argument("--ok-coverage", type=float, default=0.80)
    ap.add_argument("--ok-identity", type=float, default=0.90)
    ap.add_argument("--partial-coverage", type=float, default=0.50)
    args = ap.parse_args()

    mod = load_module(args.map_script, "map07b")
    base = mod.load_base_module(args.base_script)

    feature_types = getattr(mod, "DEFAULT_FEATURE_TYPES", None)
    if feature_types is None:
        feature_types = {
            "Initiator methionine", "Signal peptide", "Transit peptide", "Propeptide", "Chain", "Peptide",
            "Domain", "Repeat", "Region", "Coiled coil", "Motif", "Zinc finger",
            "Active site", "Binding site", "Metal binding", "Site",
            "Modified residue", "Lipidation", "Glycosylation", "Disulfide bond", "Cross-link",
            "Nucleotide binding", "DNA binding", "Calcium binding",
        }

    rows = []
    with open(args.problem_csv, newline="", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        for r in reader:
            cls = classify_problem(r)
            r["problem_class"] = cls
            if args.only_class and cls != args.only_class:
                continue
            rows.append(r)

    if args.limit:
        rows = rows[:args.limit]

    print("problem rows selected:", len(rows))
    print("write mode:", args.write)
    print("use batch root:", args.use_batch_root)

    sifts_cache = {}
    out_rows = []

    for i, r in enumerate(rows, start=1):
        print(f"[{i}/{len(rows)}] {r.get('entry_id')} {r.get('pdb_id')} chain={r.get('chain_id')} class={r.get('problem_class')}")
        try:
            info = repair_one(r, args, mod, base, sifts_cache, feature_types)
        except Exception as e:
            info = {
                "repair_status": "error",
                "entry_id": r.get("entry_id"),
                "pdb_id": r.get("pdb_id"),
                "chain_id": r.get("chain_id"),
                "json_path": r.get("json_path"),
                "repair_note": repr(e),
            }

        merged = dict(r)
        merged.update(info)
        out_rows.append(merged)

    out = Path(args.summary_out)
    out.parent.mkdir(parents=True, exist_ok=True)

    fieldnames = []
    for r in out_rows:
        for k in r.keys():
            if k not in fieldnames:
                fieldnames.append(k)

    with open(out, "w", newline="", encoding="utf-8") as f:
        w = csv.DictWriter(f, fieldnames=fieldnames, extrasaction="ignore")
        w.writeheader()
        w.writerows(out_rows)

    c = Counter(r.get("repair_status", "") for r in out_rows)
    print("repair status:", dict(c))
    print("summary:", out)


if __name__ == "__main__":
    main()
