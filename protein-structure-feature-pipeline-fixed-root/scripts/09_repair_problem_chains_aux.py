"""
09_repair_problem_chains_aux.py

Purpose
-------
Second-round repair for chains that remain PROBLEM / FAILED after
07b_sifts_alignment_fallback.py.

This script uses auxiliary files generated by CryoNet / emdbMatch.py:
  - .toRCSB  : local chain <-> original/RCSB chain mapping
  - .fasta   : current entry sequence grouping
  - .faa     : original/template chain sequences
  - .cif     : struct_ref / struct_ref_seq UniProt accession hints

Repair logic
------------
For each problem chain:
  1. Load current chain JSON and current chain.sequence.
  2. Collect auxiliary files from:
       - the JSON source_files directory
       - optional web roots
       - optional batch root
  3. Parse .toRCSB to infer local_chain -> RCSB_chain.
  4. Parse mmCIF struct_ref/struct_ref_seq to infer chain -> UniProt accession.
  5. Build a candidate UniProt accession list from:
       - SIFTS all accessions for this PDB
       - SIFTS accessions from .toRCSB-derived RCSB chain
       - mmCIF struct_ref accessions
       - previous accession if present
  6. Align current chain.sequence against candidate UniProt sequences.
  7. If best alignment is HIGH / ACCEPTABLE / PARTIAL_USABLE:
       - rewrite uniprot_mapping
       - rebuild UniProt features, Pfam, InterPro annotation
       - mark repair metadata
     Otherwise leave JSON unchanged unless --write-failed-marker is set.

Important
---------
This script assumes the current 07b_sifts_alignment_fallback.py contains
the functions previously used in your workflow:
  load_base_module, load_sifts_index, alignment_mapping_object,
  fetch_uniprot_json, extract_uniprot_features, convert_uniprot_features,
  convert_sifts_xrefs, merge_external, write_json_atomic, append_log.

Run dry-run first:
  python 09_repair_problem_chains_aux.py --limit 100

Then write:
  python 09_repair_problem_chains_aux.py --limit 100 --write
"""

import argparse
